// Function to shuffle an array (Fisher-Yates)
const shuffleArray = (array) => {
    let currentIndex = array.length, randomIndex;
    while (currentIndex !== 0) {
        randomIndex = Math.floor(Math.random() * currentIndex);
        currentIndex--;
        [array[currentIndex], array[randomIndex]] = [array[randomIndex], array[currentIndex]];
    }
    return array;
}

// Generate random people for a room
const generateRandomPeople = () => {
    const numPeople = Math.floor(Math.random() * 7) + 4; // 4 to 10 people

    // We want to ensure uniqueness in a room, so we shuffle the master list
    const shuffledImages = shuffleArray([...profileImages]);

    // Select the first 'numPeople' images from the shuffled list
    const selectedImages = shuffledImages.slice(0, numPeople);

    return selectedImages.map(avatar_url => ({
        avatar_url,
        premium: Math.random() < 0.2 // ~20% chance of being premium
    }));
};

// Helper to get random creator name based on language
const getRandomCreator = (language) => {
    const names = languageNames[language] || languageNames['Default'];
    const randomName = names[Math.floor(Math.random() * names.length)];
    return { username: randomName };
};


// Shared pool for initial rooms to ensure NO REPEATS across all rooms
let globalImagePool = shuffleArray([...profileImages]);

const generateUniqueRandomPeople = (showRings = false) => {
    // Dynamic Population: Most 4+, some 1-3
    const rand = Math.random();
    const numPeople = rand < 0.7 ? 4 : Math.floor(Math.random() * 3) + 1;

    // We want to ensure uniqueness across ALL initial rooms
    if (globalImagePool.length < numPeople) {
        // Refill if empty, but shouldn't happen with ~750 images
        globalImagePool = shuffleArray([...profileImages]);
    }

    const selectedImages = [];
    for (let i = 0; i < numPeople; i++) {
        if (globalImagePool.length > 0) {
            selectedImages.push(globalImagePool.pop());
        }
    }

    return selectedImages.map(avatar_url => ({
        avatar_url,
        premium: showRings && Math.random() < 0.25 // Only show rings if requested
    }));
};


const getRandomAvatar = () => {
    return profileImages[Math.floor(Math.random() * profileImages.length)];
};

